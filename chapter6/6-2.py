def day_name(number):
    if number== 0:
        return "Sunday"
    elif number==1:
        return "Monday"
    elif number==2:
        return "Tuseday"
    elif number==3:
        return "Wednesday"
    elif number==4:
        return "Thursday"
    elif number==5:
        return "Friday"
    elif number==6:
        return "Saturday"
    else:
        return "none"

